package com.weblabs.beans;

public class UpdateLeaves {

	private String id;
	private String Employee;
	private String Starting_At;
	private String Ending_On;
	private String Days;
	private String Reason;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmployee() {
		return Employee;
	}
	public void setEmployee(String employee) {
		Employee = employee;
	}
	public String getStarting_At() {
		return Starting_At;
	}
	public void setStarting_At(String starting_At) {
		Starting_At = starting_At;
	}
	public String getEnding_On() {
		return Ending_On;
	}
	public void setEnding_On(String ending_On) {
		Ending_On = ending_On;
	}
	public String getDays() {
		return Days;
	}
	public void setDays(String days) {
		Days = days;
	}
	public String getReason() {
		return Reason;
	}
	public void setReason(String reason) {
		Reason = reason;
	}	
	
	
}
